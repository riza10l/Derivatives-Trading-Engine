def ensure_data_dir():
    os.makedirs(DATA_DIR, exist_ok=True)


def save_sample():
    """Save bundled sample data so scripts can always load something."""
    ensure_data_dir()
    path = os.path.join(DATA_DIR, "BTC-USD_sample.csv")
    if not os.path.exists(path):
        with open(path, "w") as f:
            f.write(SAMPLE_DATA)
        print(f"[yfinance] Sample data saved to {path}")
    else:
        print(f"[yfinance] Sample data already exists at {path}")
    return path


def fetch_yfinance(symbol: str, days: int):
    """Download historical data from Yahoo Finance."""
    try:
        import yfinance as yf
    except ImportError:
        print("[yfinance] yfinance not installed. Install: pip install yfinance")
        print("[yfinance] Falling back to bundled sample data.")
        return save_sample()

    ticker = yf.Ticker(symbol)
    end = datetime.now()
    start = end - timedelta(days=days)
    df = ticker.history(start=start.strftime("%Y-%m-%d"), end=end.strftime("%Y-%m-%d"))

    if df.empty:
        print(f"[yfinance] WARNING: No data for {symbol}. Using sample.")
        return save_sample()

    # Normalize columns: keep timestamp, OHLC, volume
    df = df.reset_index()
    df["timestamp"] = df["Datetime"].dt.strftime("%Y-%m-%d %H:%M:%S")
    df = df[["timestamp", "Open", "High", "Low", "Close", "Volume"]]
    df.columns = ["timestamp", "open", "high", "low", "close", "volume"]

    # Round to 2 decimals
    for col in ["open", "high", "low", "close"]:
        df[col] = df[col].round(2)
    df["volume"] = df["volume"].round(1)

    ensure_data_dir()
    period = f"{days}d"
    path = os.path.join(DATA_DIR, f"{symbol}_{period}.csv")
    df.to_csv(path, index=False)
    print(f"[yfinance] Downloaded {len(df)} rows → {path}")
    return path


def main():
    parser = argparse.ArgumentParser(description="Download crypto price data from Yahoo Finance")
    parser.add_argument("--symbol", default="BTC-USD", help="Yahoo Finance symbol (default: BTC-USD)")
    parser.add_argument("--days", type=int, default=365, help="Days of history (default: 365)")
    args = parser.parse_args()

    path = fetch_yfinance(args.symbol, args.days)
    print(f"[yfinance] Ready: {path}")
    print(f"[yfinance] Usage: python scripts/rl_market_making.py --real-data {path}")


if __name__ == "__main__":
    main()
